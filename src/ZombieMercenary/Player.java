package ZombieMercenary;

 /**
 * @author Meade & Zepp 
 */
public class Player 
{
    private float locationX;
    private float locationY;
    
    public float getLocationX()
    {
        return locationX;
    }
    public float getLocationY()
    {
        return locationY;
    }
    public void setLocationX(float x)
    {
        locationX = x;
    }
    public void setLocationY(float y)
    {
        locationY = y;
    }
}
